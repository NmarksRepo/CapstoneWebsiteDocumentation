<h1>Joshua Horovitz and Nicholas Marks Project Proposal</h1>
    
    <p>
        <b>Project Description</b>
        <br />
        We are proposing a dynamic web site that will host video game information for users of the site. Each user will be able to view the site not logged in but to<br /> add a game to the database the user needs to be logged in. The user will be able to sort the games by different categories. Some of those include genre,<br /> developer, franchise, and price. By default, the games will be ordered alphabetically.
        We decided to make this website because there are not many video game databases widely available and the ones that currently exist are too hard to find, too hard to use, or are at the constant risk of being taken down.
        <br />
        <br />
        <b>Functionality</b>
        <br />
        Functionality of the site must include the following:
        <br />
        User can have the option to create account and log in
        <br />
        Users without account will still be able to use the website but not be able to change the database
        <br />
        Users that are logged in can Add games
        <br />
        Admins can Edit/Update/Delete games
        <br />
        All users will be able to organize the games by multiple categories
        <br />
        <br />
        <b>Development Platform</b>
        The site will be coded in ASP.NET with SQL Server as the DBMS. We find that this site is easier to build in ASP.Net rather than PHP. The site will initially be hosted on linux.neit.edu
    </p>